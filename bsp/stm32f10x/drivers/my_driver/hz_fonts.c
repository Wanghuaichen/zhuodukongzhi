uint16_t get_hz_font(uint8_t code1,uint8_t code2,uint8_t code3,char** c)
{
    int i;
    
    for( i = 0; i < 400; i++)
    {
        if(code1 == c_hz_Font[i].hz[0] && code2 == c_hz_Font[i].hz[1] && code3 == c_hz_Font[i].hz[2])
        {
            *c = (char*)&c_hz_Font[i].font[0];
            return i;
        }
    }
    
    *c = (char*)&c_hz_Font[0].font[0];
    return 0;
}
